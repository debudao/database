package entity;

public class Playwright {

	private int playwrightid;
	private String playwrightname;

	public int getPlaywrightid() {
		return playwrightid;
	}

	public void setPlaywrightid(int playwrightid) {
		this.playwrightid = playwrightid;
	}

	public String getPlaywrightname() {
		return playwrightname;
	}

	public void setPlaywrightname(String playwrightname) {
		this.playwrightname = playwrightname;
	}

}
